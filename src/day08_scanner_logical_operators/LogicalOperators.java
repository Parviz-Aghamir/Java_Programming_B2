package day08_scanner_logical_operators;

public class LogicalOperators {

    public static void main(String[] args) {
        int n = 20;
        System.out.println(n>5); // true
        System.out.println(n<10);//true
        System.out.println(n > 5 && n < 10);
        System.out.println(n>5 || n <10);


    }
}
